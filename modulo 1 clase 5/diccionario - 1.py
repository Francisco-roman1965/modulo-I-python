"""Diccionarios en Python"""

"""
Los nombres de los Key(llaves) van a ir escritos siempre en minúsculas (por buenas prácticas)
"""

estudiantes = {'nombre': "Gustavo", 'edad': 27, 'promedio': 18}

"""Agregamos elementos nuevos al diccionario"""

estudiantes['distrito'] = "San Isidro"
estudiantes['habilitado'] = True
estudiantes['nombre'] = "Fiorella"


print("Diccionario actualizado: {}".format(estudiantes))
